package miniBoard;

public class Board {
	private int no;
	private String name;
	private String title;
	private String content;
	
	public Board() {
		
	}
	public Board(int no, String name, String title, String content) {
		super();
		this.no = no;
		this.name = name;
		this.title = title;
		this.content = content;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
} // Board 클래스 - 제목, 글 저장

